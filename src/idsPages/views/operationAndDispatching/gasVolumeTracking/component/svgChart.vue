        { name: "中海油", value: "30.10" },
      ],
      contractList: [],
      pipeLineList: [],
      stationList: [],
    };
  },
  props: {
    svgData: {
      type: Object,
      default: () => {},
    },
  },
  watch: {
    svgData: {
      handler(newVal) {
        this.setData(newVal);
      },
    },
  },

  methods: {
    // 加号点击事件
    change(i) {
      this.imgList[i].open = !this.imgList[i].open;
      if (i > 8) {
        this.$emit("openTrack", i, this.imgList[i].open);
      }
    },
    // 数据获取并处理
    setData(data) {
      data.contractList.forEach((items) => {
        this.changeStr(items);
      });
      data.pipeLineList.forEach((items) => {
        this.changeStr(items);
        items.gasSourceList.forEach((item) => {
          this.changeStr(item);
        });
      });
      data.stationList.forEach((items) => {
        this.changeStr(items);
      });
      this.contractList = data.contractList;
      this.pipeLineList = data.pipeLineList;
      let stationList = ["2", "6", "9"];
      let newList = [];
      stationList.forEach((item) => {
        data.stationList.forEach((items) => {
          if (item == items.stationSort) {
            newList.push(items);
          }
        });
      });
      this.stationList = newList;
    },
    // 空串转换成"-"
    changeStr(data) {
      for (let i in data) {
        if (data[i] === "" || data[i] == null) {
          data[i] = "-";
        }
      }
    },
  },
};
</script>

